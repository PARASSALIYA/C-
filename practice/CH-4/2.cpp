#include <iostream>
using namespace std;

int main()
{
    int a, b;

    cout << "Enter A: ";
    cin >> a;
    cout << "Enter B :";
    cin >> b;

    cout << "Sum   : " << a + b;
    cout << endl;
    cout << "Sub   : " << a - b;
    cout << endl;
    cout << "mult  : " << a * b;
    cout << endl;
    cout << "div   : " << a / b;
    cout << endl;
    cout << "modul : " << a % b;
    cout << endl;
}